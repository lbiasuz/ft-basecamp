int	ft_fibonacci(int index)
{
	int	m1;
	int	m2;
	int	aux;

	m1 = 0;
	m2 = 1;
	while (index > 0)
	{
		aux = m1;
		m1 = m1 + m2;
		m2 = aux;
		index--;
	}
	return (m1);
}
