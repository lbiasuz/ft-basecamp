int	ft_atoi(char *str)
{
	int	ac;
	int	sign;

	ac = 0;
	sign = 1;
	while (*str == ' ')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		ac = ac * 10 + (*str - '0');
		str++;
	}
	return (ac * sign);
}
