int	ft_atoi(char *str)
{
	int	ac;
	int	sign;

	ac = 0;
	sign = 1;
	while
	(
		*str == ' ' || *str == '\t' || *str == '\r' ||
		*str == '\v' || *str == '\n' || *str == '\f'
	)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9') // str "76944" => 76944 
	{
		ac = ac * 10 + (*str - 48);
		str++;
	}
	return (ac * sign);
}
